public class HelloWorld {

    // Entry point of every Java application
    public static void main(String[] args) {
        
        // Prints text with a newline at the end
        System.out.println("Hello World");

        // In Java, every statement must be written inside a class
        // This follows one of the key principles of Java: Object-Oriented Programming

        // Prints text without a newline
        System.out.print("This statement prints without next line ");

        // Prints text with a newline
        System.out.println("I love Java");
    }
}
